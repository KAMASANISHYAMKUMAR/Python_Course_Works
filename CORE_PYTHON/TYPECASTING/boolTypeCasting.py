
#bool type casting using bool( )
#int -> bool
x=123
y=bool(x)
print(type(x)," ----> ", type(y) )
print("x : ",x," ----> ","y : ",y)
print("=====================")

#float -> bool
x=1.0
y=bool(x)
print(type(x)," ----> ", type(y) )
print("x : ",x," ----> ","y : ",y)
print("=====================")

#complex -> bool
x=0+0j
y=bool(x)
print(type(x)," ----> ", type(y) )
print("x : ",x," ----> ","y : ",y)
print("=====================")

#string -> bool
x="shashi"
x="True"
x="False"
x="0"
x=" "
