
''' Arithmetic Operators
            / [Division] VS // [Floor Division]


   Division operator always returns the result in
   the form of float type.
   
                  int / int -> float
                  float / int -> float
                  int / float -> float
                  float / float -> float

   Floor Division returns the result based on
   operand types, and it will return only precision
   value but not scale value.    12 .1212

                  int // int -> int
                  float // int -> float
                  int // float -> float
                  float // float -> float    
'''

print("10/3 ? : ",10/3)
print("10/3.0 ? : ",10/3.0)
print("10.0/3 ? : ",10.0/3)
print("10.3/3.0 ? : ",10.0/3.0)
print("===================")

print("10//3 ? : ",10//3)
print("10//3.0 ? : ",10//3.0)
print("10.0//3 ? : ",10.0//3)
print("10.3//3.0 ? : ",10.0//3.0)





















