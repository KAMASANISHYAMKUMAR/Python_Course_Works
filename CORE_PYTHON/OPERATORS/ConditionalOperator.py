
'''Shortcut operator
     Combination of both assignment and
     Arithmetic

     += | -= | *= | /= | %=


     Operator   Action      mean
     +=                x+=y          x=x+y
     -=                 n-=m         n=n-m
     /=                 r/=10         r=r/10
     %=              r%=10       r=r%10
     *=               n*=m          n=n*m
'''

x=10
print("x : ",x)
x+=20  #x=x+20
print("x : ",x)

n=5
m=4
n*=m #n=n*m
print("Result is : ",n)





















