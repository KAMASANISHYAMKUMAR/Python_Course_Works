

''' Membership Operator
     in : Returns True iff the given object
                  is existed in the collection or iterable

                    Syn: object in iterable
                    
     not in : Returns True iff the given object is not
     existed in the iterable | collection

                    Syn: object not in iterable

'''
lst=[10,20,30,40,50,60,70]
print("List : ",lst)
print("40 in lst ? : ",40 in lst)
print("90 in lst ? : ",90 in lst)
print("===================")
print("90 not in lst ? : ",90 not in lst)









