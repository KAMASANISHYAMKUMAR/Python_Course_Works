
x=10         #int
y=12.12   #float
z='SssiT'  #str

#Syn: print(value,value,......)
print(x,y,z) #Output: 10 12.12 SssiT

'''Note: While printing multiple values then
each value by default sep by a space.reason
default value for sep attribute [ property] in
print is a space    Eg: sep=' '

    print(value,value,......,sep=' ')
'''
print(x,y,z,sep=' ') #10 12.12 SssiT
print(x,y,z,sep=',') #10,12.12,SssiT
print(x,y,z,sep='>') #10>12.12>Sssit
print(x,y,z,sep='') #1012.12Sssit














