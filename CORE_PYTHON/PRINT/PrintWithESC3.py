




''' Esc Seq Character  | Back slash char
          - These are used to produce the result in the
          user desired fashion.

          \n        -> new Line
          \t         -> tab space
          \a        -> bell sound

          \'         -> It will print ' symbol
          \"        -> It will print " symbol
          \\         -> It will print \ symbol

Note: we use these Esc seq char in sep , end
attribute or directly in the print( )
'''

x=10
y=12.12
z="SssiT"

#output: 10'12.12'SssiT

print(x,y,z,sep='\'')
print(x,y,z,sep="\"")

