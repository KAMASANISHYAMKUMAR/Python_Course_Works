
''' Format specifiers
       - Used to represent what type of
       values to be formated during the
       output

       data type    Format Specifiers
       int                 %d
       float             %f
       String          %s

          Syn:  print("format specifier" %variable)
                    print("format specifiers" %(variable) )
'''
x=10
print(x)
print("%d" %x)
print("x val is : %d" %x)

y=12.1212
print("%f" %y)
print("y val is : %f" %y)
print("y val is : %.2f" %y)


print("%d %f" %(x,y) )
print("x val is :%d and y val is :%.2f " %(x,y))











      

















